/**
 * Pushes agents.conf and PJSIP extension config to the Asterisk server.
 * Set ASTERISK_CONFIG_API_URL (e.g. http://10.50.2.190:9999) in .env.
 * Optional: ASTERISK_CONFIG_API_KEY for auth. If URL is not set, sync is skipped.
 */

import { query } from './db.js';

const CONFIG_API_URL = process.env.ASTERISK_CONFIG_API_URL?.trim() || '';
const CONFIG_API_KEY = process.env.ASTERISK_CONFIG_API_KEY?.trim() || '';

export function isAsteriskSyncConfigured() {
  return !!CONFIG_API_URL;
}

async function postConfig(path, body) {
  if (!CONFIG_API_URL) return { ok: true, skipped: true };
  const url = `${CONFIG_API_URL.replace(/\/$/, '')}${path}`;
  const headers = { 'Content-Type': 'application/json' };
  if (CONFIG_API_KEY) headers['X-Config-API-Key'] = CONFIG_API_KEY;
  const res = await fetch(url, {
    method: 'POST',
    headers,
    body: JSON.stringify(body),
    signal: AbortSignal.timeout(15000),
  });
  const text = await res.text();
  if (!res.ok) {
    throw new Error(`Asterisk config API ${path} returned ${res.status}: ${text}`);
  }
  return { ok: true, skipped: false, status: res.status, body: text };
}

/**
 * Build agents.conf content from DB (all users with role 5 and phone_login_number set).
 * Pushes to Asterisk config API and triggers reload.
 */
export async function syncAgentsToAsterisk() {
  try {
    const rows = await query(
      `SELECT phone_login_number, username FROM users
       WHERE role = 5 AND phone_login_number IS NOT NULL AND TRIM(phone_login_number) != ''
       ORDER BY phone_login_number`
    );
    const lines = ['; Auto-generated by PBX app - do not edit by hand', '[general]', ''];
    for (const r of rows) {
      const id = String(r.phone_login_number).trim();
      const name = (r.username || id).trim();
      lines.push(`[${id}]`);
      lines.push(`fullname=${name}`);
      lines.push('');
    }
    const content = lines.join('\n');
    const result = await postConfig('/config/agents', { content });
    if (result.skipped) return;
    console.log('Asterisk agents.conf synced successfully');
  } catch (err) {
    console.error('Asterisk agents sync error:', err.message || err);
    throw err;
  }
}

/**
 * Build PJSIP config (endpoint + auth + aor per extension) from sip_extensions.
 * Pushes to Asterisk config API and triggers reload.
 */
export async function syncExtensionsToAsterisk() {
  try {
    const rows = await query(
      'SELECT name, secret, context, host, dtmfmode FROM sip_extensions ORDER BY tenant_id, name'
    );
    const blocks = [
      '; Auto-generated by PBX app - do not edit by hand',
      '; Include this file from pjsip.conf: #include "pjsip_custom.conf"',
      '',
      '; Dummy endpoint for ARI queue failover redirect (PJSIP channel must redirect to PJSIP endpoint)',
      '[queue-failover]',
      'type=endpoint',
      'context=QueueFailover',
      'aors=queue-failover',
      'disallow=all',
      'allow=ulaw',
      '',
      '[queue-failover]',
      'type=aor',
      'max_contacts=1',
      '; Static contact so aor list is non-empty (redirect would otherwise crash in res_pjsip)',
      'contact=sip:127.0.0.1:9',
      '',
    ];
    for (const r of rows) {
      const name = String(r.name).trim();
      const secret = (r.secret && String(r.secret).trim()) || name;
      const context = (r.context && String(r.context).trim()) || 'from-internal';
      const transport = 'transport-udp';
      blocks.push(`[${name}]`);
      blocks.push('type=endpoint');
      blocks.push(`transport=${transport}`);
      blocks.push(`context=${context}`);
      blocks.push('disallow=all');
      blocks.push('allow=ulaw');
      blocks.push(`auth=${name}`);
      blocks.push(`aors=${name}`);
      blocks.push('');
      blocks.push(`[${name}]`);
      blocks.push('type=auth');
      blocks.push('auth_type=userpass');
      blocks.push(`username=${name}`);
      blocks.push(`password=${secret}`);
      blocks.push('');
      blocks.push(`[${name}]`);
      blocks.push('type=aor');
      blocks.push('max_contacts=1');
      blocks.push('');
    }
    const content = blocks.join('\n');
    const result = await postConfig('/config/pjsip', { content });
    if (result.skipped) return;
    console.log('Asterisk PJSIP extensions synced successfully');
  } catch (err) {
    console.error('Asterisk extensions sync error:', err.message || err);
    throw err;
  }
}

/**
 * Build PJSIP trunk config (endpoint + auth + aor, and optionally registration + identify) from sip_trunks.
 * config_json shape: { type: 'endpoint'|'registration', transport?, context?, username, password, realm?,
 *   aor_contact?, identify_match? (string or string[]), server_uri?, client_uri?, contact_user? }
 * Pushes to Asterisk config API and triggers reload.
 */
export async function syncTrunksToAsterisk() {
  try {
    const rows = await query(
      'SELECT trunk_name, config_json FROM sip_trunks ORDER BY tenant_id, trunk_name'
    );
    const lines = [
      '; Auto-generated by PBX app - do not edit by hand',
      '; Include this file from pjsip.conf: #include "pjsip_trunks_custom.conf"',
      '',
    ];
    for (const r of rows) {
      const name = String(r.trunk_name || '').trim();
      if (!name) continue;
      let cfg = r.config_json;
      // If stored as string, try JSON first so identify_match is applied for inbound trunks
      if (typeof cfg === 'string') {
        const trimmed = cfg.trim();
        if (trimmed.startsWith('{')) {
          try {
            cfg = JSON.parse(cfg);
          } catch {
            cfg = null;
          }
        }
        if (cfg == null || typeof cfg !== 'object') {
          const raw = trimmed || cfg;
          if (raw) {
            if (raw && !String(raw).startsWith('[')) lines.push(`[${name}]`);
            lines.push(String(raw));
            lines.push('');
          }
          continue;
        }
      }
      if (!cfg || typeof cfg !== 'object') cfg = {};
      const type = (cfg.type || 'endpoint').toLowerCase();
      const transport = (cfg.transport && String(cfg.transport).trim()) || 'transport-udp';
      const context = (cfg.context && String(cfg.context).trim()) || 'from-external';
      const username = (cfg.username && String(cfg.username).trim()) || name;
      const password = (cfg.password && String(cfg.password).trim()) || '';
      const realm = (cfg.realm && String(cfg.realm).trim()) || '';

      if (type === 'registration') {
        const serverUri = (cfg.server_uri && String(cfg.server_uri).trim()) || '';
        const clientUri = (cfg.client_uri && String(cfg.client_uri).trim()) || '';
        const contactUser = (cfg.contact_user && String(cfg.contact_user).trim()) || '';
        if (!serverUri || !clientUri) continue;
        lines.push(`[${name}]`);
        lines.push('type=registration');
        lines.push(`transport=${transport}`);
        lines.push(`outbound_auth=${name}`);
        lines.push(`server_uri=${serverUri}`);
        lines.push(`client_uri=${clientUri}`);
        if (contactUser) lines.push(`contact_user=${contactUser}`);
        lines.push('retry_interval=60');
        lines.push('');
        lines.push(`[${name}]`);
        lines.push('type=auth');
        lines.push('auth_type=userpass');
        lines.push(`username=${username}`);
        lines.push(`password=${password}`);
        if (realm) lines.push(`realm=${realm}`);
        lines.push('');
        const regMatch = cfg.identify_match;
        const regMatches = regMatch == null ? [] : Array.isArray(regMatch) ? regMatch : [regMatch];
        const regMatchStrs = regMatches.map((m) => String(m).trim()).filter(Boolean);
        if (regMatchStrs.length > 0) {
          lines.push(`[${name}]`);
          lines.push('type=identify');
          lines.push(`endpoint=${name}`);
          for (const m of regMatchStrs) lines.push(`match=${m}`);
          lines.push('');
        }
      } else {
        lines.push(`[${name}]`);
        lines.push('type=endpoint');
        lines.push(`transport=${transport}`);
        lines.push(`context=${context}`);
        lines.push('disallow=all');
        lines.push('allow=ulaw');
        lines.push(`outbound_auth=${name}`);
        lines.push(`aors=${name}`);
        lines.push('');
        lines.push(`[${name}]`);
        lines.push('type=auth');
        lines.push('auth_type=userpass');
        lines.push(`username=${username}`);
        lines.push(`password=${password}`);
        if (realm) lines.push(`realm=${realm}`);
        lines.push('');
        const aorContact = (cfg.aor_contact && String(cfg.aor_contact).trim()) || '';
        lines.push(`[${name}]`);
        lines.push('type=aor');
        if (aorContact) {
          lines.push(`contact=${aorContact}`);
        } else {
          lines.push('max_contacts=0');
        }
        lines.push('');
        const match = cfg.identify_match;
        const matches = match == null ? [] : Array.isArray(match) ? match : [match];
        const matchStrs = matches.map((m) => String(m).trim()).filter(Boolean);
        if (matchStrs.length > 0) {
          lines.push(`[${name}]`);
          lines.push('type=identify');
          lines.push(`endpoint=${name}`);
          for (const m of matchStrs) lines.push(`match=${m}`);
          lines.push('');
        }
      }
    }
    const content = lines.join('\n');
    const result = await postConfig('/config/pjsip-trunks', { content });
    if (result.skipped) return;
    console.log('Asterisk PJSIP trunks synced successfully');
  } catch (err) {
    console.error('Asterisk trunks sync error:', err.message || err);
    throw err;
  }
}

/**
 * Build the complete callcentre.conf dialplan from the database.
 * Covers: from-pstn, from-internal, inbound, WhereToGO, Queue, IVR, DTMFIVR,
 *         NormalIVR, TimeCondition, SipExtension, VoiceMail, Announcement,
 *         AgentLogin, callstatus, ManualCallByExtension, ring-agent,
 *         notify-incoming, notify-answered, notify-hangup, hangup-customer,
 *         TMain (blind transfer).
 * Pushes to Asterisk config receiver and triggers dialplan reload.
 */
export async function syncDialplanToAsterisk() {
  try {
    const appUrl = process.env.ASTERISK_APP_URL?.trim()
      || `http://127.0.0.1:${process.env.PORT || 3001}/api/asterisk/`;
    const stasisApp = process.env.ASTERISK_STASIS_APP?.trim() || 'queue-dashboard';
    const recPath = process.env.ASTERISK_RECORDING_PATH?.trim() || '/var/spool/asterisk/monitor';

    const trunks = await query('SELECT trunk_name FROM sip_trunks ORDER BY id LIMIT 1');
    const defaultTrunk = trunks[0]?.trunk_name || 'default-trunk';

    const lines = [];
    const L = (s) => lines.push(s);

    // ---- globals ----
    L('; === Auto-generated callcentre.conf - do not edit by hand ===');
    L('[globals]');
    L(`APP_URL=${appUrl}`);
    L(`AGENT_LOGIN=\${APP_URL}AgentLogin?`);
    L(`AGENT_LOGOUT=\${APP_URL}AgentLogout?`);
    L(`AGENT_LOGIN_SUCCESS=\${APP_URL}AgentLoginSuccess?`);
    L(`INBOUND_ROUTE=\${APP_URL}InboundRoute?`);
    L(`QUEUE_MEMBERS=\${APP_URL}QueueMembers?`);
    L(`CALL_INCOMING=\${APP_URL}IncomingCall?`);
    L(`CALL_ANSWERED=\${APP_URL}CallAnswered?`);
    L(`CALL_HANGUP=\${APP_URL}CallHangup?`);
    L(`TRANSFERRED_ANSWERED=\${APP_URL}TransferredCallAnswered?`);
    L(`RECORDING=\${APP_URL}Recording?`);
    L(`IVR_DETAIL=\${APP_URL}IVRDetail?`);
    L(`IVR_OPTION_ROUTE=\${APP_URL}IVROptionRoute?`);
    L(`TIME_CONDITION=\${APP_URL}TimeCondition?`);
    L(`VOICEMAIL_DETAIL=\${APP_URL}VoicemailDetail?`);
    L(`SOUND_FILE_PATH=\${APP_URL}SoundFilePath?`);
    L(`EXTENSION_FAILOVER=\${APP_URL}ExtensionFailover?`);
    L(`STASIS_APP=${stasisApp}`);
    L(`REC_PATH=${recPath}`);
    L(`DEFAULT_TRUNK=${defaultTrunk}`);
    L('');

    // ---- [from-trunk] ----
    L('[from-trunk]');
    L('exten => _X.,1,NoOp(Inbound call from trunk DID=${EXTEN})');
    L(' same => n,Set(DNID=${EXTEN})');
    L(' same => n,Set(CallerNumber=${CALLERID(num)})');
    L(' same => n,Goto(inbound,${EXTEN},1)');
    L('');

    // ---- [from-internal] ----
    L('[from-internal]');
    L('; Extension-to-extension dialing');
    L('exten => _XXXX,1,NoOp(Internal call to ${EXTEN})');
    L(' same => n,Set(RecName=${STRFTIME(${EPOCH},,%Y%m%d-%H%M%S)}-${CALLERID(num)}-${EXTEN})');
    L(` same => n,MixMonitor(\${REC_PATH}/internal/\${RecName}.wav)`);
    L(' same => n,Dial(PJSIP/${EXTEN},30)');
    L(' same => n,Hangup()');
    L('');
    L('; Outbound: dial 9 + number via default trunk');
    L('exten => _9.,1,NoOp(Outbound call to ${EXTEN:1} via ${DEFAULT_TRUNK})');
    L(' same => n,Set(RecName=${STRFTIME(${EPOCH},,%Y%m%d-%H%M%S)}-${CALLERID(num)}-${EXTEN:1})');
    L(` same => n,MixMonitor(\${REC_PATH}/outbound/\${RecName}.wav)`);
    L(' same => n,Dial(PJSIP/${EXTEN:1}@${DEFAULT_TRUNK},60)');
    L(' same => n,Hangup()');
    L('');

    // ---- [inbound] ----
    L('[inbound]');
    L('exten => _X.,1,NoOp(Inbound route lookup DID=${EXTEN})');
    L(' same => n,Set(CallStartTime=${EPOCH})');
    L(` same => n,Set(RouteInfo=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${INBOUND_ROUTE}DID=\${EXTEN}&CallerNumber=\${CALLERID(num)}&CallUniqueID=\${UNIQUEID}")})`);
    L(' same => n,Set(ARRAY(CallStatus,Destination_Action,Destination_Value,UserID,CDRID,CampaignName)=${RouteInfo})');
    L(' same => n,GotoIf($["${CallStatus}"="OK"]?route)');
    L(' same => n,Hangup()');
    L(' same => n(route),Set(TIMEOUT(absolute)=7200)');
    L(' same => n,Set(CHANNEL(hangup_handler_push)=callstatus,s,1)');
    L(' same => n,Answer()');
    L(' same => n,Goto(WhereToGO,${Destination_Action},1)');
    L('');

    // ---- [WhereToGO] ----
    L('[WhereToGO]');
    L('; action 0 = hangup');
    L('exten => 0,1,NoOp(WhereToGO: Hangup)');
    L(' same => n,Hangup()');
    L('');
    L('; action 1 = announcement');
    L('exten => 1,1,NoOp(WhereToGO: Announcement)');
    L(' same => n,Goto(Announcement,s,1)');
    L('');
    L('; action 2 = queue');
    L('exten => 2,1,NoOp(WhereToGO: Queue ${Destination_Value})');
    L(' same => n,Goto(Queue,s,1)');
    L('');
    L('; action 3 = ivr');
    L('exten => 3,1,NoOp(WhereToGO: IVR ${Destination_Value})');
    L(' same => n,Goto(IVR,s,1)');
    L('');
    L('; action 4 = voicemail');
    L('exten => 4,1,NoOp(WhereToGO: Voicemail ${Destination_Value})');
    L(' same => n,Goto(VoiceMail,s,1)');
    L('');
    L('; action 5 = time condition');
    L('exten => 5,1,NoOp(WhereToGO: TimeCondition ${Destination_Value})');
    L(' same => n,Goto(TimeCondition,s,1)');
    L('');
    L('; action 9 = extension');
    L('exten => 9,1,NoOp(WhereToGO: Extension ${Destination_Value})');
    L(' same => n,Goto(SipExtension,s,1)');
    L('');

    // ---- [Queue] ----
    L('[Queue]');
    L('exten => s,1,NoOp(Queue: ${Destination_Value})');
    L(` same => n,Stasis(\${STASIS_APP},\${Destination_Value},\${UNIQUEID})`);
    L(' same => n,Goto(QueueFailover,s,1)');
    L('');

    // ---- [QueueFailover] (ARI sets Destination_Action, Destination_Value before redirect here) ----
    L('[QueueFailover]');
    L('exten => s,1,NoOp(Queue failover: ${Destination_Action} ${Destination_Value})');
    L(' same => n,Goto(WhereToGO,${Destination_Action},1)');
    L('');

    // ---- [IVR] ----
    L('[IVR]');
    L('exten => s,1,NoOp(IVR: ${Destination_Value})');
    L(' same => n,Set(IVRID=${Destination_Value})');
    L(` same => n,Set(IVRInfo=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${IVR_DETAIL}IVRID=\${IVRID}")})`);
    L(' same => n,Set(ARRAY(IVRStatus,IVRType,IVRGreeting,IVRValidKeys,IVRTimeout,IVRNoinputRetries,IVRInvalidRetries,IVRDefaultDestType,IVRDefaultDestValue,IVRNoinputDestType,IVRNoinputDestValue,IVRInvalidDestType,IVRInvalidDestValue)=${IVRInfo})');
    L(' same => n,GotoIf($["${IVRStatus}"!="OK"]?hangup)');
    L(' same => n,GotoIf($["${IVRType}"="normal"]?normal)');
    L(' same => n,Goto(DTMFIVR,s,1)');
    L(' same => n(normal),Goto(NormalIVR,s,1)');
    L(' same => n(hangup),Hangup()');
    L('');

    // ---- [DTMFIVR] ----
    L('[DTMFIVR]');
    L('exten => s,1,NoOp(DTMF IVR: ${IVRID})');
    L(' same => n,Set(IVR_ATTEMPTS=0)');
    L(' same => n,Set(IVR_NOINPUT=0)');
    L(' same => n(loop),GotoIf($[${IVR_ATTEMPTS}>=${IVRInvalidRetries}]?invalid_dest)');
    L(' same => n,Set(IVR_ATTEMPTS=$[${IVR_ATTEMPTS}+1])');
    L(' same => n,GotoIf($["${IVRGreeting}"=""]?read)');
    L(' same => n,Playback(${IVRGreeting})');
    L(' same => n(read),Read(IVR_KEY,,1,,,${IVRTimeout})');
    L(' same => n,GotoIf($["${IVR_KEY}"=""]?noinput)');
    L(` same => n,Set(IVROptionInfo=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${IVR_OPTION_ROUTE}IVRID=\${IVRID}&Key=\${IVR_KEY}")})`);
    L(' same => n,Set(ARRAY(OptStatus,Destination_Action,Destination_Value)=${IVROptionInfo})');
    L(' same => n,GotoIf($["${OptStatus}"="OK"]?WhereToGO,${Destination_Action},1)');
    L(' same => n,Playback(pbx-invalid)');
    L(' same => n,Goto(loop)');
    L(' same => n(noinput),Set(IVR_NOINPUT=$[${IVR_NOINPUT}+1])');
    L(' same => n,GotoIf($[${IVR_NOINPUT}>=${IVRNoinputRetries}]?noinput_dest)');
    L(' same => n,Goto(loop)');
    L(' same => n(noinput_dest),GotoIf($["${IVRNoinputDestType}"="0"]?hangup)');
    L(' same => n,Set(Destination_Action=${IVRNoinputDestType})');
    L(' same => n,Set(Destination_Value=${IVRNoinputDestValue})');
    L(' same => n,Goto(WhereToGO,${Destination_Action},1)');
    L(' same => n(invalid_dest),GotoIf($["${IVRInvalidDestType}"="0"]?hangup)');
    L(' same => n,Set(Destination_Action=${IVRInvalidDestType})');
    L(' same => n,Set(Destination_Value=${IVRInvalidDestValue})');
    L(' same => n,Goto(WhereToGO,${Destination_Action},1)');
    L(' same => n(hangup),Hangup()');
    L('');

    // ---- [NormalIVR] ----
    L('[NormalIVR]');
    L('exten => s,1,NoOp(Normal IVR: ${IVRID})');
    L(' same => n,GotoIf($["${IVRGreeting}"=""]?route)');
    L(' same => n,Playback(${IVRGreeting})');
    L(' same => n(route),GotoIf($["${IVRDefaultDestType}"="0"]?hangup)');
    L(' same => n,Set(Destination_Action=${IVRDefaultDestType})');
    L(' same => n,Set(Destination_Value=${IVRDefaultDestValue})');
    L(' same => n,Goto(WhereToGO,${Destination_Action},1)');
    L(' same => n(hangup),Hangup()');
    L('');

    // ---- [TimeCondition] ----
    L('[TimeCondition]');
    L('exten => s,1,NoOp(TimeCondition: ${Destination_Value})');
    L(` same => n,Set(TCInfo=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${TIME_CONDITION}TCID=\${Destination_Value}")})`);
    L(' same => n,Set(ARRAY(TCStatus,Destination_Action,Destination_Value)=${TCInfo})');
    L(' same => n,GotoIf($["${TCStatus}"="OK"]?WhereToGO,${Destination_Action},1)');
    L(' same => n,Hangup()');
    L('');

    // ---- [SipExtension] (extension no-answer -> failover, same flow as queue) ----
    L('[SipExtension]');
    L('exten => s,1,NoOp(Direct to extension ${Destination_Value})');
    L(' same => n,Set(RecName=${STRFTIME(${EPOCH},,%Y%m%d-%H%M%S)}-${CALLERID(num)}-${Destination_Value})');
    L(` same => n,MixMonitor(\${REC_PATH}/inbound/\${RecName}.wav)`);
    L(` same => n,Set(CHANNEL(hangup_handler_push)=hangup-customer,s,1)`);
    L(' same => n,Set(PJSIP_HEADER(add,Alert-Info)=info=alert-autoanswer)');
    L(' same => n,Set(PJSIP_HEADER(add,Call-Info)=<sip:>\\;answer-after=0)');
    L(` same => n,Dial(PJSIP/\${Destination_Value},30,b(notify-incoming^s^1(\${CHANNEL(name)}^\${UNIQUEID}^\${CALLERID(num)}^\${Destination_Value}^direct^\${CampaignName}))B(notify-answered^s^1(\${UNIQUEID}^\${Destination_Value})))`);
    L(' same => n,GotoIf($["${DIALSTATUS}"="ANSWER"]?callstatus,s,1)');
    L(` same => n,Set(ExtFailover=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${EXTENSION_FAILOVER}ExtensionName=\${Destination_Value}")})`);
    L(' same => n,Set(ARRAY(ExtStatus,Destination_Action,Destination_Value)=${ExtFailover})');
    L(' same => n,GotoIf($["${ExtStatus}"="OK"]?WhereToGO,${Destination_Action},1)');
    L(' same => n,Hangup()');
    L('');

    // ---- [VoiceMail] ----
    L('[VoiceMail]');
    L('exten => s,1,NoOp(Voicemail: ${Destination_Value})');
    L(` same => n,Set(VMInfo=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${VOICEMAIL_DETAIL}VMID=\${Destination_Value}")})`);
    L(' same => n,Set(ARRAY(VMStatus,VMMailbox,VMGreeting,VMMaxDuration)=${VMInfo})');
    L(' same => n,GotoIf($["${VMStatus}"!="OK"]?hangup)');
    L(' same => n,GotoIf($["${VMGreeting}"=""]?record)');
    L(' same => n,Playback(${VMGreeting})');
    L(' same => n(record),Set(VMRecFile=${REC_PATH}/voicemail/${VMMailbox}-${STRFTIME(${EPOCH},,%Y%m%d-%H%M%S)}.wav)');
    L(' same => n,Record(${VMRecFile},${VMMaxDuration},5)');
    L(' same => n,Hangup()');
    L(' same => n(hangup),Hangup()');
    L('');

    // ---- [Announcement] ----
    L('[Announcement]');
    L('exten => s,1,NoOp(Announcement: ${Destination_Value})');
    L(` same => n,Set(AnnFile=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${SOUND_FILE_PATH}SoundID=\${Destination_Value}")})`);
    L(' same => n,GotoIf($["${AnnFile}"=""]?done)');
    L(' same => n,Playback(${AnnFile})');
    L(' same => n(done),Hangup()');
    L('');

    // ---- [AgentLogin] ----
    L('[AgentLogin]');
    L('exten => s,1,NoOp(Agent ${AgentNumber} Request For Login)');
    L(' same => n,Answer()');
    L(' same => n,Set(AgentStatus=LoginInitiated)');
    L(` same => n,Set(AgentInformation=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${AGENT_LOGIN}AgentID=\${AgentNumber}&AgentStatus=\${AgentStatus}")})`);
    L(' same => n,Set(ARRAY(CallStatus,CallCause)=${AgentInformation})');
    L(' same => n,Authenticate(${AgentPassword})');
    L(' same => n,Set(AgentStatus=LoginCompleted)');
    L(` same => n,Set(LoginResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${AGENT_LOGIN_SUCCESS}AgentID=\${AgentNumber}&AgentStatus=\${AgentStatus}")})`);
    L(` same => n,Stasis(\${STASIS_APP},login,\${AgentNumber})`);
    L(' same => n,NoOp(Agent exited Stasis)');
    L(' same => n,Hangup()');
    L('');

    // ---- [callstatus] (hangup handler) ----
    L('[callstatus]');
    L('exten => s,1,NoOp(Call status handler)');
    L(' same => n,Set(CallEndTime=${EPOCH})');
    L(` same => n,Set(HangupResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_HANGUP}AgentID=\${AgentNumber}&UniqueID=\${CDRID}&Status=completed")})`);
    L(` same => n,ExecIf($["\${RecName}"!=""]?Set(RecResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${RECORDING}UniqueID=\${CDRID}&Path=\${RecName}")}))`);
    L(' same => n,Return()');
    L('');

    // ---- [ManualCallByExtension] ----
    L('[ManualCallByExtension]');
    L('exten => s,1,NoOp(Manual call by ${AgentNumber} to ${CustomerNumber})');
    L(' same => n,Set(RecName=${STRFTIME(${EPOCH},,%Y%m%d-%H%M%S)}-${AgentNumber}-${CustomerNumber})');
    L(` same => n,MixMonitor(\${REC_PATH}/manual/\${RecName}.wav)`);
    L(' same => n,Dial(PJSIP/${CustomerNumber}@${DEFAULT_TRUNK},60)');
    L(' same => n,Hangup()');
    L('');

    // ---- [auto-answer-queue] ----
    L('[auto-answer-queue]');
    L('exten => s,1,NoOp(Auto-answer queue call for agent ${AGENT_EXTEN})');
    L(' same => n,Dial(PJSIP/${AGENT_EXTEN},45,b(auto-answer-sub^s^1))');
    L('');

    // ---- [auto-answer-sub] ----
    L('[auto-answer-sub]');
    L('exten => s,1,Set(PJSIP_HEADER(add,Alert-Info)=info=alert-autoanswer)');
    L(' same => n,Set(PJSIP_HEADER(add,Call-Info)=<sip:>\\;answer-after=0)');
    L(' same => n,Return()');
    L('');

    // ---- [ring-agent] ----
    L('[ring-agent]');
    L('exten => s,1,NoOp(Ring agent ${ARG1} for queue ${ARG2})');
    L(' same => n,Set(PHASE3_AGENT_ID=${ARG1})');
    L(' same => n,Set(PHASE3_QUEUE_NAME=${ARG2})');
    L(' same => n,Set(PHASE3_UNIQUE_ID=${UNIQUEID})');
    L(' same => n,Set(RecName=${STRFTIME(${EPOCH},,%Y%m%d-%H%M%S)}-${CALLERID(num)}-${ARG1})');
    L(` same => n,MixMonitor(\${REC_PATH}/queue/\${RecName}.wav)`);
    L(` same => n,Set(CHANNEL(hangup_handler_push)=hangup-customer,s,1)`);
    L(' same => n,Set(PJSIP_HEADER(add,Alert-Info)=info=alert-autoanswer)');
    L(' same => n,Set(PJSIP_HEADER(add,Call-Info)=<sip:>\\;answer-after=0)');
    L(` same => n,Dial(PJSIP/\${ARG1},30,b(notify-incoming^s^1(\${CHANNEL(name)}^\${UNIQUEID}^\${CALLERID(num)}^\${ARG1}^\${ARG2}^\${CampaignName}))B(notify-answered^s^1(\${UNIQUEID}^\${ARG1})))`);
    L(' same => n,Goto(callstatus,s,1)');
    L('');

    // ---- [notify-incoming] ----
    L('[notify-incoming]');
    L('exten => s,1,NoOp(Notify incoming: channel=${ARG1} uid=${ARG2} caller=${ARG3} agent=${ARG4} queue=${ARG5} campaign=${ARG6})');
    L(` same => n,Set(NotifyResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_INCOMING}AgentID=\${ARG4}&UniqueID=\${ARG2}&CustomerNumber=\${ARG3}&QueueName=\${ARG5}&ChannelID=\${ARG1}&CampaignName=\${ARG6}")})`);
    L(' same => n,Return()');
    L('');

    // ---- [notify-answered] ----
    L('[notify-answered]');
    L('exten => s,1,NoOp(Notify answered: uid=${ARG1} agent=${ARG2})');
    L(` same => n,Set(AnswerResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_ANSWERED}AgentID=\${ARG2}&UniqueID=\${ARG1}")})`);
    L(' same => n,Return()');
    L('');

    // ---- [notify-hangup] ----
    L('[notify-hangup]');
    L('exten => s,1,NoOp(Notify hangup: uid=${ARG1} agent=${ARG2})');
    L(` same => n,Set(HangupNotify=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_HANGUP}AgentID=\${ARG2}&UniqueID=\${ARG1}&Status=completed")})`);
    L(' same => n,Return()');
    L('');

    // ---- [hangup-customer] ----
    L('[hangup-customer]');
    L('exten => s,1,NoOp(Customer hangup handler)');
    L(` same => n,Set(HangupResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_HANGUP}UniqueID=\${UNIQUEID}&Status=completed")})`);
    L(` same => n,ExecIf($["\${RecName}"!=""]?Set(RecResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${RECORDING}UniqueID=\${UNIQUEID}&Path=\${RecName}")}))`);
    L(' same => n,Return()');
    L('');

    // ---- [TMain] (blind transfer; CDRID and AgentNumber set by app before redirect) ----
    L('[TMain]');
    L('exten => _X.,1,NoOp(Blind transfer to ${EXTEN} CDRID=${CDRID})');
    L(' same => n,Set(TransferStatus=1)');
    L(' same => n,Set(TransferAgent=${EXTEN})');
    L(` same => n,Set(TransferNotify=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_HANGUP}AgentID=\${AgentNumber}&UniqueID=\${CDRID}&Status=transferred")})`);
    L(' same => n,Set(CHANNEL(hangup_handler_push)=transfer-hangup,s,1)');
    L(' same => n,Dial(PJSIP/${EXTEN},30,M(transfer-answered^${CDRID}^${EXTEN}))');
    L(' same => n,Hangup()');
    L('');
    L('; ---- [transfer-answered] (B-leg answered; notify app so agent dashboard shows connected) ----');
    L('[transfer-answered]');
    L('exten => s,1,NoOp(Transferred call answered UniqueID=${ARG1} AgentID=${ARG2})');
    L(` same => n,Set(AnsweredNotify=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${TRANSFERRED_ANSWERED}UniqueID=\${ARG1}&AgentID=\${ARG2}")})`);
    L(' same => n,Return()');
    L('');
    L('; ---- [transfer-hangup] (transferred call ended; clear target agent status) ----');
    L('[transfer-hangup]');
    L('exten => s,1,NoOp(Transfer call hangup CDRID=${CDRID} Agent=${TransferAgent})');
    L(` same => n,Set(HangupResult=\${SHELL(curl -s --connect-timeout 3 --max-time 5 "\${CALL_HANGUP}AgentID=\${TransferAgent}&UniqueID=\${CDRID}&Status=completed")})`);
    L(' same => n,Return()');
    L('');

    // ---- [BargeMe] (supervisor whisper: supervisor hears agent, can talk to agent only; customer does not hear) ----
    L('[BargeMe]');
    L('exten => s,1,NoOp(BargeMe whisper on ${BargeChannel})');
    L(' same => n,ChanSpy(${BargeChannel},qw)');
    L(' same => n,Hangup()');
    L('');

    const content = lines.join('\n');
    const result = await postConfig('/config/dialplan', { content });
    if (result.skipped) return;
    console.log('Asterisk dialplan (callcentre.conf) synced successfully');
  } catch (err) {
    console.error('Asterisk dialplan sync error:', err.message || err);
    throw err;
  }
}

/**
 * Run all syncs (agents, extensions, trunks, dialplan) and return a result for the API.
 * Use for manual "Sync to Asterisk" from Super Admin.
 */
export async function syncAllToAsterisk() {
  if (!CONFIG_API_URL) {
    return { success: false, skipped: true, message: 'ASTERISK_CONFIG_API_URL is not set in .env. Sync skipped.' };
  }
  const errors = [];
  try {
    await syncAgentsToAsterisk();
  } catch (e) {
    errors.push(`Agents: ${e.message || e}`);
  }
  try {
    await syncExtensionsToAsterisk();
  } catch (e) {
    errors.push(`Extensions: ${e.message || e}`);
  }
  try {
    await syncTrunksToAsterisk();
  } catch (e) {
    errors.push(`Trunks: ${e.message || e}`);
  }
  try {
    await syncDialplanToAsterisk();
  } catch (e) {
    errors.push(`Dialplan: ${e.message || e}`);
  }
  if (errors.length) {
    return { success: false, skipped: false, message: errors.join('; '), errors };
  }
  return { success: true, skipped: false, message: 'Config synced to Asterisk (agents, extensions, trunks, dialplan). Run "dialplan reload" and "module reload res_pjsip.so" if needed.' };
}
